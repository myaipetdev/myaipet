// One-off: turn the 8 template still examples into short autoplay clips via
// Grok image-to-video (grok-imagine-video), animating the already-deployed
// /studio_examples/<id>.jpg stills so the motion matches the preview art.
//
//   cd web && node scripts/gen-studio-example-videos.mjs
//
// Run on EC2 (valid production GROK key). Emits src/lib/studio/example-videos.ts.

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";

function loadKey() {
  for (const f of [".env.production", ".env.local", ".env"]) {
    if (!existsSync(f)) continue;
    for (const line of readFileSync(f, "utf8").split("\n")) {
      const m = line.match(/^\s*(GROK_API_KEY|XAI_API_KEY)\s*=\s*(.+)\s*$/);
      if (m) return m[2].trim().replace(/^["']|["']$/g, "");
    }
  }
  throw new Error("GROK_API_KEY not found");
}
const KEY = loadKey();
const APP = "https://app.myaipet.ai";
const DURATION = 5;

// Motion/scene only — the subject (our white Pomeranian mascot) is carried by
// the i2v anchor still, so we don't re-describe the character here.
const TEMPLATES = {
  "birthday-party":  "pastel confetti drifting in slow motion, the pet leans toward a small frosted cake with a lit candle, warm cinematic light",
  "new-year-wish":   "colorful fireworks bloom across the night sky and reflect in the pet's eyes, gentle camera pull-back",
  "daily-vlog":      "the pet gives a curious head tilt toward the camera in soft natural light, leaves rustling outside the window",
  "morning-stretch": "the pet does a slow big stretch with paws extended, then a sleepy yawn, warm morning light",
  "noir-detective":  "slow dolly through rain at night under a neon street lamp, 1940s film-noir mood, selective red neon",
  "anime-opening":   "cherry-blossom petals swirl past, fur blowing in the wind, dramatic backlight, heroic anime opening motion",
  "dance-loop":      "the pet bobs its head rhythmically to a beat, happy expression, colorful disco lights, looping motion",
  "reaction-shock":  "the pet's eyes grow wide in dramatic shock, ears perk up, quick zoom-in on the face",
};

async function submit(prompt, imageUrl) {
  const res = await fetch("https://api.x.ai/v1/videos/generations", {
    method: "POST",
    headers: { "Content-Type": "application/json", Authorization: `Bearer ${KEY}` },
    body: JSON.stringify({
      model: "grok-imagine-video", prompt, duration: DURATION,
      aspect_ratio: "16:9", resolution: "720p", image_url: imageUrl,
    }),
  });
  if (!res.ok) throw new Error(`submit ${res.status}: ${(await res.text()).slice(0, 160)}`);
  const d = await res.json();
  const id = d.request_id || d.id;
  if (!id) throw new Error("no request_id");
  return id;
}

async function poll(reqId, deadlineMs = 240_000) {
  const end = Date.now() + deadlineMs;
  while (Date.now() < end) {
    await new Promise(r => setTimeout(r, 5000));
    const res = await fetch(`https://api.x.ai/v1/videos/${reqId}`, { headers: { Authorization: `Bearer ${KEY}` } });
    if (!res.ok) continue;
    const d = await res.json();
    if (["done", "complete", "completed"].includes(d.status)) {
      const url = d.video?.url || d.url || d.data?.[0]?.url;
      if (url) return url;
      throw new Error("completed but no url");
    }
    if (["failed", "expired"].includes(d.status)) throw new Error(d.error || d.status);
  }
  throw new Error("timeout");
}

async function download(url, file) {
  const r = await fetch(url);
  if (!r.ok) throw new Error(`download ${r.status}`);
  writeFileSync(resolve("public/studio_examples", file), Buffer.from(await r.arrayBuffer()));
}

const manifest = {};
for (const [id, prompt] of Object.entries(TEMPLATES)) {
  try {
    process.stdout.write(`  ${id} … submit `);
    const reqId = await submit(prompt, `${APP}/studio_examples/${id}.jpg`);
    process.stdout.write("polling ");
    const url = await poll(reqId);
    await download(url, `${id}.mp4`);
    manifest[id] = `/studio_examples/${id}.mp4`;
    console.log("ok");
  } catch (e) {
    console.log(`FAILED — ${e.message}`);
  }
}

writeFileSync(
  resolve("src/lib/studio/example-videos.ts"),
  `// AUTO-GENERATED by scripts/gen-studio-example-videos.mjs — do not edit.\n` +
  `// Short Grok i2v clips for the template gallery (animate the still examples).\n` +
  `export const TEMPLATE_EXAMPLE_VIDEOS: Record<string, string> = ${JSON.stringify(manifest, null, 2)};\n`
);
console.log(`\nWrote example-videos.ts — ${Object.keys(manifest).length}/8 videos`);
